public class ArithmeticOperators {
    public static void main (String[] args) {
        int a = 10, b = 5;
        int sum = a + b;       // 15
        int difference = a - b; // 5
        int product = a * b;    // 50
        int quotient = a / b;   // 2
        int remainder = a % b;  // 0

        a++; // 11
        b--; // 4
    }
}
